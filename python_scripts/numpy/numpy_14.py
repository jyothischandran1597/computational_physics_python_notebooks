# Accessing 1-D numpy array elements using index

import numpy as np

arr = np.array([10, 15, 25, 35, 45, 55, 65])

print("The array is ",arr)
print("The length of the array is ",len(arr))
print("Value at index 0: ",arr[0])
print("Value at index 2: ",arr[2])
print("Value at index 5: ",arr[5])
print("Values from index 2 to 4: ",arr[2:5])