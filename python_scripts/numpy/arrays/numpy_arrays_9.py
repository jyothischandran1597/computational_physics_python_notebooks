# Python ones array

import numpy as np

print("Python array of ones from numpy.ones()\n")

print(np.ones(3), '\n Dimensions:', np.zeros(3).shape, '\n')
print(np.ones((2, 2)), '\n Dimensions:', np.zeros((2,2)).shape, '\n')

print(np.ones((2, 3)), '\n Dimensions:', np.zeros((2,2)).shape, '\n')
print(np.ones((4, 7)), '\n Dimensions:', np.zeros((4,7)).shape, '\n')
print(np.ones((3, 2, 7)), '\n Dimensions:', np.zeros((3,2,7)).shape, '\n')